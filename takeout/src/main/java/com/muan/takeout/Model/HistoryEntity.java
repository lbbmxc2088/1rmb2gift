public class HistoryEntity {
    private String imgUrl;
    private String goodsName;
    private String goodsTimes;
    private String goodsQihao;
    private String winnerName;
    private String winTimes;

    public String getImgUrl() {
        return imgUrl;
    }

    public void setImgUrl(String imgUrl) {
        this.imgUrl = imgUrl;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public String getGoodsTimes() {
        return goodsTimes;
    }

    public void setGoodsTimes(String goodsTimes) {
        this.goodsTimes = goodsTimes;
    }

    public String getGoodsQihao() {
        return goodsQihao;
    }

    public void setGoodsQihao(String goodsQihao) {
        this.goodsQihao = goodsQihao;
    }

    public String getWinnerName() {
        return winnerName;
    }

    public void setWinnerName(String winnerName) {
        this.winnerName = winnerName;
    }

    public String getWinTimes() {
        return winTimes;
    }

    public void setWinTimes(String winTimes) {
        this.winTimes = winTimes;
    }
}
